const menuReducer = (state = initalState, action) => {
  switch (action.type) {
    case actionTypes.ADD_TO_CART:
      return {
        ...state,
        addedToCart: state.addedToCart.concat({
          name: action.name,
          weight: action.weight,
          info: action.info,
          price: action.price,
          image: action.image
        }),
        cartTotalSum: state.cartTotalSum.concat(parseInt(action.price))
      };

    case actionTypes.DELETE_ARTICLE_FROM_CART:
      const id = action.id;
      const newArray = [...state.addedToCart];
      newArray.splice(id, 1);
      const newPrice = [...state.cartTotalSum];
      newPrice.splice(id, 1);
      return {
        ...state,
        addedToCart: newArray,
        cartTotalSum: newPrice
      };

    case actionTypes.ORDER_COMPLETED:
      return {
        ...state,
        addedToCart: [],
        cartTotalSum: []
      };

    case actionTypes.USER_INFO:
      return {
        ...state,
        userEmail: action.email,
        userUid: action.uid
      };

    case actionTypes.DELIVERY_INFO:
      return {
        ...state,
        usersNickOrName: action.usersName,
        usersAddress: action.usersAddress,
        usersPhoneNumber: action.phoneNum
      };

    case actionTypes.IS_THERE_AN_ADDRESS:
      return {
        ...state,
        fetchedUsersAddress: action.address
      };

    default:
      return state;
  }
};

export default menuReducer;
